void solve(){
    int n,x;
    cin>>n>>x;

    vector<int>arr(n);
    for(int i=0;i<n;i++) cin>>arr[i]; // input

    vector<int>dp(x+1); // creating dp
    dp[0]=1; // base case 

    for(int i=0;i<n;i++){  // for the given number traversal
        for(int j=1;j<=x;j++){   // for given array traversal
            if(j-arr[i]>=0)
            dp[j] = (dp[j]+dp[j-arr[i]])%mod; 
        }
    }
    cout<<dp[x]<<endl; // answer 

}
