const int INF = 1e15;

/* write core logic here */
void solve(){
    int n, m, q;
    cin >> n >> m >> q;

    vector<vector<int>> dist(n+1, vector<int>(n+1, INF));

    // Distance to self = 0
    for (int i = 1; i <= n; i++) dist[i][i] = 0;

    // Read edges
    for (int i = 0; i < m; i++) {
        int u, v, w;
        cin >> u >> v >> w;
        dist[u][v] = min(dist[u][v], w); // if multiple edges
        dist[v][u] = min(dist[v][u], w); // undirected
    }

    // Floyd-Warshall
    for (int k = 1; k <= n; ++k) {
        for (int i = 1; i <= n; ++i) {
            for (int j = 1; j <= n; ++j) {
                if (dist[i][k] < INF && dist[k][j] < INF)
                    dist[i][j] = min(dist[i][j], dist[i][k] + dist[k][j]);
            }
        }
    }

    // Process queries
    while (q--) {
        int a, b;
        cin >> a >> b;
        if (dist[a][b] == INF) cout << -1 << endl;
        else cout << dist[a][b] << endl;
    }
}
