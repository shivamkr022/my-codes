void solve(){
    int n;
    cin>>n;

    vector<int>arr(n);
    for(int i=0;i<n;i++) cin>>arr[i]; // input

    int ans =0;  // answer
    int ng=0;  //negative count
    int pos =0;// positive count

    for(int i=0;i<n;i++){
        if(arr[i]==1) pos++; // counting
        else ng++;
    }

    while(!((pos>=ng) and ng%2==0)){ // we need pos and neg equal or pos>neg and neg%2==0 so multi always be +ve
        ng--;
        pos++;
        ans++;
    }
    cout<<ans<<endl;
}
