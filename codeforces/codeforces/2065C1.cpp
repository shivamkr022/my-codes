void solve(){
    int n, m;
    cin >> n >> m;
    vector<int> a(n), b(m);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    for (int i = 0; i < m; i++) {
        cin >> b[i];
    }
    sort(b.begin(), b.end());
    int prev = -1e9;
    for (int i = 0; i < n; i++){   
        auto it = lower_bound(b.begin(), b.end(), prev + a[i]);
        if (it == b.end()){  // no element is found
            if (a[i] < prev){
                cout<<"NO"<<endl;
                return;
            }
        }
        else if (a[i] < prev || a[i] > *it - a[i]){
            a[i] = *it - a[i];
        }
        prev = a[i];
    }
    cout<<"YES"<<endl;
}
