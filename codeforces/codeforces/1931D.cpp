void solve(){
    int n,x,y;
    cin>>n>>x>>y;
    vector<int>arr(n);
    for(auto & i:arr) cin>>i;
    map<int, map<int, int>> mp;
    int ans = 0;
    for (auto & ele :arr) {
        int v = (x - ele % x) % x;  // modular arithmetic proves   
        int b = ele % y;   // modular arithmetic proves   
        ans += mp[v][b];  
        mp[ele % x][ele % y]++;
    }
    cout << ans << endl;
}
